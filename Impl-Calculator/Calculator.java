class Calculator {
	double add (double num1, double num2) {
		double num;
		num = num1 + num2;
		return num;
	}
	double sub (double num1, double num2) {
		double num;
		num = num1 - num2;
		return num;
	}
	double mul (double num1, double num2) {
		double num;
		num = num1 * num2;
		return num;
	}
	double div (double num1, double num2) {
		double num;
		num = num1 / num2;
		return num;
	}
}